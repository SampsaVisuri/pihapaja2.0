document.getElementById("show").addEventListener("click", function () {

    document.getElementById("refe4").style.display = 'flex';
    document.getElementById("refe5").style.display = 'flex';
    document.getElementById("refe6").style.display = 'flex';
    document.getElementById("refe7").style.display = 'flex';
    document.getElementById("refe8").style.display = 'flex';
    document.getElementById("refe9").style.display = 'flex';

    document.getElementById("show").style.display = 'none';
    document.getElementById("hide").style.display = 'block';
})

document.getElementById("hide").addEventListener("click", function () {

    document.getElementById("refe4").style.display = 'none';
    document.getElementById("refe5").style.display = 'none';
    document.getElementById("refe6").style.display = 'none';
    document.getElementById("refe7").style.display = 'none';
    document.getElementById("refe8").style.display = 'none';
    document.getElementById("refe9").style.display = 'none';

    document.getElementById("show").style.display = 'block';
    document.getElementById("hide").style.display = 'none';
})




